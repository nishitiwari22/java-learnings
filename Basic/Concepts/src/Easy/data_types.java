
public class data_types {
	public static void main(String[] args) {

		System.out.println("Hello World!");

		// This is comment
		/*
		 * This is
		 * Multiple
		 * line comment
		 */

		int age = 21;

		// Data Types
		// Integral DT
		byte b = 127;
		short s = 100;
		int i = 10000;
		long l = 10000000l;

		System.out.println("Value of l is: " + l);

		// Floating type
		float f = 100.4577375F;
		double d = 100.478490289743864;

		double price = 15_25_000.112345337;
		System.out.println(price);

		// Non numeric DT
		char c = 'a';
		char c1 = 98;
		boolean bool = false;
		System.out.println(c);
		System.out.println(bool);

	}
}
